    private iPartsGuiDaimlerLangSelectionBox langSelectionBox;

    /**
     * Erzeugt eine Instanz von iPartsDaimlerLangSelectionForm.
     * Den $$internalCreateGui$$() Aufruf nicht ändern!
     */
    public iPartsDaimlerLangSelectionForm(AbstractJavaViewerFormIConnector dataConnector, AbstractJavaViewerForm parentForm,
                                          String initialLanguages) {
        super(dataConnector, parentForm);
        $$internalCreateGui$$(null);
        postCreateGui();
        langSelectionBox.init(getProject(), initialLanguages);
        langSelectionBox.addEventListener(new EventListener(Event.ON_CHANGE_EVENT) {
            @Override
            public void fire(Event event) {
                enableButtons();
            }
        });
        enableButtons();
    }

    /**
     * Hier kann eigener Code stehen der gerufen wird wenn die Instanz
     * erzeugt wurde.
     */
    private void postCreateGui() {
        langSelectionBox = new iPartsGuiDaimlerLangSelectionBox();
        ConstraintsBorder comboboxConstraints = new ConstraintsBorder();
        langSelectionBox.setConstraints(comboboxConstraints);
        mainWindow.panelMain.addChild(langSelectionBox);

        mainWindow.pack();
    }

    @Override
    public AbstractGuiControl getGui() {
        return mainWindow.panelMain;
    }

    public String getSelectedLangs() {
        if (mainWindow.getModalResult() == ModalResult.OK) {
            return langSelectionBox.getSelectedLangs();
        }
        return "";
    }

    public List<Language> getSelectedLanguages() {
        if (mainWindow.getModalResult() == ModalResult.OK) {
            return langSelectionBox.getSelectedLanguages();
        }
        return new DwList<>();
    }

    public ModalResult showModal() {
        return mainWindow.showModal();
    }

    @Override
    public void close() {
        mainWindow.setVisible(false);
        super.close();
    }

    private void enableButtons() {
        boolean isEnabled = !langSelectionBox.getSelectedLangs().isEmpty();
        mainWindow.buttonpanel.setButtonEnabled(GuiButtonOnPanel.ButtonType.OK, isEnabled);
    }

